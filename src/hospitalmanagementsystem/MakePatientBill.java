    private void clearfield() {
        txtPatientAdmitionId.setText("");
        txtTreatMent.setText("");
        TxtTest.setText("");
        TxtOther.setText("");
        TxtTotal.setText("");
        DefaultTableModel modela = (DefaultTableModel) jTable2.getModel();
        DefaultTableModel modelb = (DefaultTableModel) jTable1.getModel();
         int rowCounta = modela.getRowCount();
         int rowCountb = modelb.getRowCount();
        for (int i = rowCounta- 1; i >= 0; i--) {
            modela.removeRow(i);
        }
        for (int i = rowCountb- 1; i >= 0; i--) {
            modelb.removeRow(i);
        }
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // update data to patientbill table:
        if (txtTreatMent.getText().equals("") || TxtTest.getText().equals("") || TxtOther.getText().equals("") || TxtTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "All text field must be fill !!");
        } else {
            patientBill pb = new patientBill();
            pb.setAdmition_id(Integer.parseInt(txtPatientAdmitionId.getText()));
            pb.setTreatment(Integer.parseInt(txtTreatMent.getText()));
            pb.setTest(Integer.parseInt(TxtTest.getText()));
            pb.setOther(Integer.parseInt(TxtOther.getText()));
            pb.setTotal(Integer.parseInt(TxtTotal.getText()));
            dbOperation db = new dbOperation();
            boolean result = db.insert_data_patientbill_tabe(pb);
            if (result) {
                JOptionPane.showMessageDialog(this, "Succesfully updated !!");
                clearfield();
            } else {
                JOptionPane.showMessageDialog(this, " Error occur while run insert_data_patientbill_tabe method !!");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

