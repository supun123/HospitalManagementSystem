private void update_table_treatment(String table[][]) {//update treatment table in gui
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        int rowCount = model.getRowCount();

        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        System.out.println("BB" + table.length);
        for (int i = 0; i < table.length; i++) {//add rows to the table
            model.addRow(new Object[]{table[i][0], table[i][1], table[i][2], table[i][3]});
        }

    }

    private void update_table_testing(String table[][]) {//update testing table in gui
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        int rowCount = model.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        System.out.println("BB" + table.length);
        for (int i = 0; i < table.length; i++) {//add rows to the table
            model.addRow(new Object[]{table[i][0], table[i][1]});
        }

    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // get Data from database 
        dbOperation db = new dbOperation();
        int result = db.checkPatientId_given_table(txtPatientAdmitionId.getText(), "patient");
        if (result == 0) {
            String table1[][] = db.select_treatment_make_salary(txtPatientAdmitionId.getText());
            update_table_treatment(table1);//update table in the gui
            String table2[][] = db.select_Testing_data_from_patient(txtPatientAdmitionId.getText());
            update_table_testing(table2);
        } else if (result == 1) {
            JOptionPane.showMessageDialog(this, "patient id does not exist !!");
        } else if (result == 2) {
            JOptionPane.showMessageDialog(this, "Error occor while run checkPatientId_given_table method !!");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

