    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // going to display give_medicine jframe
        int result0 = db.update_patient_Table("conform_consultant_id", getConsultent_id(), Integer.parseInt(txtPatientAdmitionId.getText()));//update patient deatails
        if (result0 == 1) {
            JOptionPane.showMessageDialog(this, "Error occur while  execute update_patient_Table method !");//if error occur while update update_patient_Table display massage
        }

        int result1 = db.checkPatientId_given_table(txtPatientAdmitionId.getText(), "patient");//check patienid whether exist in patient table
        if (result1 == 0) {//if result1=0 patient id is exist 
            int result2 = db.checkPatientId_given_table(txtPatientAdmitionId.getText(), "personal_details");////check patienid whether exist in personal_details table
            if (result2 == 0) {//if result2 =0 the id is exist in the table & update personal_details table
                db.update_patient_personal_deatails(Integer.parseInt(txtAdmissionId.getText()), Integer.parseInt(txtAge.getText()), txtDiseases.getText(), txtPhysical_disabilities.getText(), txtAllergic_Medicines.getText(), txtPreviousOperationHistory.getText());
                give_medicine gm = new give_medicine();
                gm.setDate(date);
                gm.setAddmission(Integer.parseInt(txtAdmissionId.getText()));
                gm.setConsultant_id(Consultent_id);
                gm.setVisible(true);
            } else if (result2 == 1) {//if result2 =1 the id is not exist in the table & add personal_details table
                db.add_personal_deatails(Integer.parseInt(txtAdmissionId.getText()), Integer.parseInt(txtAge.getText()), txtDiseases.getText(), txtPhysical_disabilities.getText(), txtAllergic_Medicines.getText(), txtPreviousOperationHistory.getText());
                give_medicine gm = new give_medicine();
                gm.setDate(date);
                gm.setAddmission(Integer.parseInt(txtAdmissionId.getText()));
                gm.setConsultant_id(Consultent_id);
                gm.setVisible(true);
            } else if (result2 == 2) {//if result2 =0 error occur while run checkPatientId_given_table method
                JOptionPane.showMessageDialog(this, "Error occur while  execute checkPatientId_given_table method with personal_details Table!");
            }
        } else if (result1 == 1) {//result1 == 1 can not find admission_id in patient Table
            JOptionPane.showMessageDialog(this, "can't find admission_id in patient Table ");
        } else {// if result1 other one  Error occur while  execute checkPatientId_given_table method
            JOptionPane.showMessageDialog(this, "Error occur while  execute checkPatientId_given_table method with patient Table! ");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

