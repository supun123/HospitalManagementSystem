    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed

        int result = db.checkPatientId_given_table(txtPatientAdmitionId.getText(), "patient");//check patienid whether exist
        if (result == 0) {//if  method run successfully and patienid is exist result=0
            String s[] = db.get_patient_personol_details(txtPatientAdmitionId.getText());//get personal deatails to string array
            int Length = s.length;
            txtAdmissionId.setText(s[0]);
            txtAge.setText(s[1]);
            txtDiseases.setText(s[2]);
            txtPhysical_disabilities.setText(s[3]);
            txtAllergic_Medicines.setText(s[4]);
            txtPreviousOperationHistory.setText(s[5]);
        } else if (result == 1) {//if  method run successfully result=0 &patienid does not exist
            JOptionPane.showMessageDialog(this, "Patient Admition Id  doest not exist !");
        } else {//if checkPatientId_given_table  method not run successfully display massage
            JOptionPane.showMessageDialog(this, "Error occur while  execute sql Query!");
        }


    }//GEN-LAST:event_btnOKActionPerformed

