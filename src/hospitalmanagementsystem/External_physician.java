package hospitalmanagementsystem;

/**
 *
 * @author A C E R
 */
public class External_physician {
    private int admission_id;
    private String nic;
    private String first_name;
    private String last_name;
    private String mobile_no;

    /**
     * @return the admission_id
     */
    public int getAdmission_id() {
        return admission_id;
    }

    /**
     * @param admission_id the admission_id to set
     */
    public void setAdmission_id(int admission_id) {
        this.admission_id = admission_id;
    }

    /**
     * @return the nic
     */
    public String getNic() {
        return nic;
    }

    /**
     * @param nic the nic to set
     */
    public void setNic(String nic) {
        this.nic = nic;
    }

    /**
     * @return the first_name
     */
    public String getFirst_name() {
        return first_name;
    }

    /**
     * @param first_name the first_name to set
     */
    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    /**
     * @return the last_name
     */
    public String getLast_name() {
        return last_name;
    }

    /**
     * @param last_name the last_name to set
     */
    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    /**
     * @return the mobile_no
     */
    public String getMobile_no() {
        return mobile_no;
    }

    /**
     * @param mobile_no the mobile_no to set
     */
    public void setMobile_no(String mobile_no) {
        this.mobile_no = mobile_no;
    }
    
    
}
